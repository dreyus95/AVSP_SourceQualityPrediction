#include <stdio.h>
#include <string.h>

int T, N, S, P;
int t[10];
int ans = 0;
void solve(int a, int b, int c) {
    int kk = 0;
    int k = 0;
    if (a == 0) {
        if (t[0] % 3 == 0 && t[0]/3 >= P)  k = 1;
        if ((t[0] - 1) % 3 == 0 && t[0] >= 1) {
            if ((t[0] - 1)/3 + 1 >= P)  k = 1;
        }
        if ((t[0] - 2) % 3 == 0 && t[0] >= 2) {
            if ((t[0] - 2)/3 + 1 >= P)  k = 1;
        }
    } 
    if (a == 1) {
        if ((t[0] - 2) % 3 == 0 && (t[0] - 2)/3 + 2 >= P && t[0] >= 2)  k = 1;
        if ((t[0] - 3) % 3 == 0 && t[0] >= 3) {
            if ((t[0] - 3)/3 + 2 >= P)  k = 1;
        }
        if ((t[0] - 4) % 3 == 0 && t[0] >= 4) {
            if ((t[0] - 4)/3 + 2 >= P)  k = 1;
        }
    }
    kk += k;
    k = 0;
    if (N >= 2 && b == 0) {
        if (t[1] % 3 == 0 && t[1]/3 >= P)  k = 1;
        if ((t[1] - 1) % 3 == 0 && t[1] >= 1) {
            if ((t[1] - 1)/3 + 1 >= P)  k = 1;
        }
        if ((t[1] - 2) % 3 == 0 && t[1] >= 2) {
            if ((t[1] - 2)/3 + 1 >= P)  k = 1;
        }
    }
    if (N >= 2 && b == 1) {
        if ((t[1] - 2) % 3 == 0 && (t[1] - 2)/3 + 2 >= P && t[1] >= 2)  k = 1;
        if ((t[1] - 3) % 3 == 0 && t[1] >= 3) {
            if ((t[1] - 3)/3 + 2 >= P)  k = 1;
        }
        if ((t[1] - 4) % 3 == 0 && t[1] >= 4) {
            if ((t[1] - 4)/3 + 2 >= P)  k = 1;
        }
    }
    kk += k;
    k = 0;
    if (N >= 3 && c == 0) {
        if (t[2] % 3 == 0 && t[2]/3 >= P)  k = 1;
        if ((t[2] - 1) % 3 == 0 && t[2] >= 1) {
            if ((t[2] - 1)/3 + 1 >= P)  k = 1;
        }
        if ((t[2] - 2) % 3 == 0 && t[2] >= 2) {
            if ((t[2] - 2)/3 + 1 >= P)  k = 1;
        }
    } 
    if (N >= 3 && c == 1) {
        if ((t[2] - 2) % 3 == 0 && (t[2] - 2)/3 + 2 >= P && t[2] >= 2)  k = 1;
        if ((t[2] - 3) % 3 == 0 && t[2] >= 3) {
            if ((t[2] - 3)/3 + 2 >= P)  k = 1;
        }
        if ((t[2] - 4) % 3 == 0 && t[2] >= 4) {
            if ((t[2] - 4)/3 + 2 >= P)  k = 1;
        }
    }
    kk += k;
    k = 0;
    if (kk > ans) ans = kk;
}

int main() {
    scanf("%d\n",&T);
    for (int i = 0; i < T; i++) {
        scanf("%d %d %d", &N, &S, &P);
        for (int j = 0; j < N; j++) {
            scanf("%d", &t[j]);
        }
        scanf("\n");
        ans = 0;
        if (S == 0) {
            solve(0, 0, 0);
        } else if (S == 1) {
            solve(1, 0, 0);
            if (N >= 2) solve(0, 1, 0);
            if (N >= 3) solve(0, 0, 1);
        } else if (S == 2) {
            solve(1, 1, 0);
            if (N >= 3) solve(0, 1, 1);
            if (N >= 3) solve(1, 0 ,1);
        } else if (S == 3) {
            solve(1, 1, 1);
        }
        printf("Case #%d: %d\n", i+1, ans);
    }
    return 0;

}





